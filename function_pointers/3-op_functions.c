#include "3-calc.h"

/**
* op_add - first function.
* @a: value.
* @b: value.
* Return: zero on succes.
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - second function.
* @a: value.
* @b: value.
* Return: zero on succes.
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - third function.
* @a: value.
* @b: value.
* Return: zero on succes.
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - fourth function.
* @a: value.
* @b: value.
* Return: zero on succes.
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - fifth function.
* @a: value.
* @b: value.
* Return: zero on succes.
*/

int op_mod(int a, int b)
{
	return (a % b);
}
