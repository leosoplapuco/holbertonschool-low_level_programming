#include <stdio.h>
#include <limits.h>
#include "main.h"

/**
* print_last_digit - Getting the last digit of a number.
* @num: Value of the number.
* Return: zero on success.
*/

int print_last_digit(int num)
{
	int r;
	r = print_last_digit(num);
	_putchar ("0" + r);
	_putchar ('\n');
	return (0);
}
